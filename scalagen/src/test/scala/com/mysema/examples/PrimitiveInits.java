package com.mysema.examples;

public class PrimitiveInits {

    double a = 3;

    void doSomething() {
        // byte
        byte b = 0;
        // char
        char c = 0;
        // int
        int i = 2;
        // long
        long l1 = 1;
        long l2 = 2l;
        // float
        float f1 = 3f;
        float f2 = 3;
        // double
        double d1 = 2d;
        double d2 = 2.0;
        double d3 = 2;
        System.out.println(a + d1);
    }

}
